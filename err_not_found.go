package std

import (
	"fmt"
)

// ---------------------------------------------------------------------------------------------------------------------
// Struct
// ---------------------------------------------------------------------------------------------------------------------

type ErrorNotFound struct {
	msg string
}

// ---------------------------------------------------------------------------------------------------------------------
// Create
// ---------------------------------------------------------------------------------------------------------------------

// NewErrorNotFoundFf
//
// Panics in case of empty arguments:
//   - msg
func NewErrorNotFoundFf(msg string, msgArgs ...any) ErrorNotFound {
	if msg == "" {
		panic("`msg` must not be empty")
	}

	return ErrorNotFound{msg: fmt.Sprintf(msg, msgArgs...)}
}

// ---------------------------------------------------------------------------------------------------------------------
// State
// ---------------------------------------------------------------------------------------------------------------------

func (e ErrorNotFound) Error() string {
	return e.msg
}

